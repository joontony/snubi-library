package org.snubi.lib;

import com.opencsv.bean.CsvBindByName;
public class ModelDemography {
	
	@CsvBindByName(column="##")
    private String no;
 
    @CsvBindByName(column="병록번호")
    private String parentNo;
    
    @CsvBindByName(column="성명")
    private String name;
    
    @CsvBindByName(column="생년월일")
    private String birth;
    
    @CsvBindByName(column="성별")
    private String gender;
    
    @CsvBindByName(column="혈액형(ABO)")
    private String bloodABO;
    
    @CsvBindByName(column="혈액형(RH)")
    private String bloodRH;
    
    @CsvBindByName(column="혈액투석최초시작일")
    private String hemodialysisDate;
    
    @CsvBindByName(column="본원혈액투석최초시작일")
    private String hemodialysisHereDate;
    
    @CsvBindByName(column="체중측정일자")
    private String weightDate;
    
    @CsvBindByName(column="체중")
    private int weight;
    
    @CsvBindByName(column="신장측정일자")
    private String heightDate;
    
    @CsvBindByName(column="신장")
    private int height;

	public String getNo() {
		return no;
	}

	public String getParentNo() {
		return parentNo;
	}

	public String getName() {
		return name;
	}

	public String getBirth() {
		return birth;
	}

	public String getGender() {
		return gender;
	}

	public String getBloodABO() {
		return bloodABO;
	}

	public String getBloodRH() {
		return bloodRH;
	}

	public String getHemodialysisDate() {
		return hemodialysisDate;
	}

	public String getHemodialysisHereDate() {
		return hemodialysisHereDate;
	}

	public String getWeightDate() {
		return weightDate;
	}

	public int getWeight() {
		return weight;
	}

	public String getHeightDate() {
		return heightDate;
	}

	public int getHeight() {
		return height;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public void setParentNo(String parentNo) {
		this.parentNo = parentNo;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setBirth(String birth) {
		this.birth = birth;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public void setBloodABO(String bloodABO) {
		this.bloodABO = bloodABO;
	}

	public void setBloodRH(String bloodRH) {
		this.bloodRH = bloodRH;
	}

	public void setHemodialysisDate(String hemodialysisDate) {
		this.hemodialysisDate = hemodialysisDate;
	}

	public void setHemodialysisHereDate(String hemodialysisHereDate) {
		this.hemodialysisHereDate = hemodialysisHereDate;
	}

	public void setWeightDate(String weightDate) {
		this.weightDate = weightDate;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}

	public void setHeightDate(String heightDate) {
		this.heightDate = heightDate;
	}

	public void setHeight(int height) {
		this.height = height;
	}
    
    
}
