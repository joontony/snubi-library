package org.snubi.lib.model.xls;

import java.util.List;

public class ModelConductToExcelElement {

	String strSubjectId;
	String strSubmitDate;
	List<Object> listDataObject;

	public ModelConductToExcelElement() {

	}
	public ModelConductToExcelElement(String strSubjectId, String strSubmitDate, List<Object> listDataObject) {

		this.strSubjectId = strSubjectId;
		this.strSubmitDate = strSubmitDate;
		this.listDataObject = listDataObject;
	}
	public String getStrSubjectId() {
		return strSubjectId;
	}
	public String getStrSubmitDate() {
		return strSubmitDate;
	}
	public List<Object> getListDataObject() {
		return listDataObject;
	}
	public void setStrSubjectId(String strSubjectId) {
		this.strSubjectId = strSubjectId;
	}
	public void setStrSubmitDate(String strSubmitDate) {
		this.strSubmitDate = strSubmitDate;
	}
	public void setListDataObject(List<Object> listDataObject) {
		this.listDataObject = listDataObject;
	}
	public void addListDataObject(Object dataObject) {
		this.listDataObject.add(dataObject);
	}
	@Override
	public String toString() {
		return "ModelConductToExcelElement [strSubjectId=" + strSubjectId + ", strSubmitDate=" + strSubmitDate
				+ ", listDataObject=" + listDataObject + "]";
	}


}
