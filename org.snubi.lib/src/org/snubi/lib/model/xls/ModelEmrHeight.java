public class ModelEmrHeight {
	
	long seq;
	String patient;	
	String height;
	String date;
	
	public long getSeq() {
		return seq;
	}
	public String getPatient() {
		return patient;
	}
	public String getHeight() {
		return height;
	}
	public String getDate() {
		return date;
	}
	public void setSeq(long seq) {
		this.seq = seq;
	}
	public void setPatient(String patient) {
		this.patient = patient;
	}
	public void setHeight(String height) {
		this.height = height;
	}
	public void setDate(String date) {
		this.date = date;
	}
	@Override
	public String toString() {
		return "ModelEmrHeight [seq=" + seq + ", patient=" + patient + ", height=" + height + ", date=" + date + "]";
	}	
}
